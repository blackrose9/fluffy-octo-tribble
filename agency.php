            function checkSliderLoad(){

                var hidden = $('#block').is(":hidden");

                if (!hidden) {

                    if (wasHidden) {
                        location.reload();
                    }

                    if (!stopCheck) {
                        if (flag == totalSliders) {
                            stopCheck = true;
                            addSliderExtras();
                            checkAgentView();

