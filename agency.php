                                $('#no-content').show();
                                $('#top-tabs').hide();
                            }
                            $('body').css('overflow-y', 'auto');
                            $('.loader-wrapper').fadeOut(200);
                        }
                    } else {
                        clearInterval(interval);
                    }
                } else {
                    wasHidden = true;
                }
            }

            interval = setInterval(checkSliderLoad,500);
        });
